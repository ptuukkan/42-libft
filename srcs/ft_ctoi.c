int	ft_ctoi(char c)
{
	if (c >= '0' && c <= '9')
		return (c - 48);
	if (c >= 'a' && c <= 'f')
		return (c - 87);
	if (c >= 'A' && c <= 'F')
		return (c - 55);
	return (-1);
}
